package au.krystinam.mybudgetplanner.model;

public class BillHolder {

	private String billHolderName;

	public BillHolder(String billHolderName) {
		super();
		this.billHolderName = billHolderName;
	}

	public String getBillHolderName() {
		return billHolderName;
	}

	public void setBillHolderName(String billHolderName) {
		this.billHolderName = billHolderName;
	}
	
}
